package kt.vo;

public class HostVO {
	
	private long hostid;
	private String host;
	private String name;
	private int status;
	
	public long getHostid() {
		return hostid;
	}
	public void setHostid(long hostid) {
		this.hostid = hostid;
	}
	public String getHost() {
		return host;
	}
	public void setHost(String host) {
		this.host = host;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getStatus() {
		return status;
	}
	public void setStatus(int status) {
		this.status = status;
	}
	
}
